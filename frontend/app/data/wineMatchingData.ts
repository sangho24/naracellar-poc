// 매칭 함수
// ========================
export function matchWines(account: Account, allWines: Wine[]): MatchResult[] {
  const results = allWines.map((wine) => {
    // 가격대 적합도 (0~100)
    const mid = (account.priceRange.min + account.priceRange.max) / 2;
    const priceScore =
      wine.priceRetail >= account.priceRange.min &&
      wine.priceRetail <= account.priceRange.max
        ? 100
        : Math.max(
            0,
            100 - (Math.abs(wine.priceRetail - mid) / mid) * 100
          );

    // 카테고리 적합도 - preferredCategory와 와인 country/grape/type 텍스트 매칭
    const categoryScore = account.preferredCategory.some(
      (cat) =>
        wine.country.includes(cat.replace(" 레드", "").replace(" 화이트", "")) ||
        wine.grape.toLowerCase().includes(cat.toLowerCase()) ||
        wine.type.includes(cat) ||
        cat.includes(wine.country) ||
        (cat.toLowerCase().includes("내추럴") && wine.stockDays > 120) ||
        (cat.toLowerCase().includes("오렌지") && wine.type === "오렌지") ||
        cat.toLowerCase().includes("전품종") ||
        (cat.toLowerCase().includes("가성비") && wine.priceRetail < 30000)
    )
      ? 85 + Math.random() * 15
      : 20 + Math.random() * 30;

    // 업종 궁합
    const industryMap: Record<string, string[]> = {
      이탈리안레스토랑: ["이탈리아"],
      프렌치레스토랑: ["프랑스"],
      일식레스토랑: ["화이트", "스파클링"],
      와인바: [],
      비스트로: ["프랑스", "이탈리아"],
      호텔레스토랑: [],
      대형마트: [],
      편의점: [],
      백화점: [],
      와인전문점: [],
      온라인플랫폼: [],
      리조트: ["로제", "스파클링"],
      비어바: [],
    };

    // 타입 내 공백 제거 후 매핑 키 탐색
    const accountTypeKey = account.type.replace(/\s/g, "");
    const preferredCountries =
      Object.entries(industryMap).find(([k]) =>
        accountTypeKey.includes(k)
      )?.[1] ?? [];

    const industryScore =
      preferredCountries.length === 0
        ? 50 + Math.random() * 30
        : preferredCountries.some(
            (c) => wine.country.includes(c) || wine.type.includes(c)
          )
        ? 75 + Math.random() * 25
        : 25 + Math.random() * 25;

    // 재고 소진 긴급도 (Long Tail 우선)
    const urgencyScore = Math.min(100, (wine.stockDays / 365) * 100);

    // 마진 기여도
    const marginScore = wine.margin * 100;

    const totalScore = Math.round(
      priceScore * 0.3 +
        categoryScore * 0.25 +
        industryScore * 0.2 +
        urgencyScore * 0.15 +
        marginScore * 0.1
    );

    // 추천 이유 생성
    const reasons: string[] = [];
    if (priceScore >= 70)
      reasons.push(
        `객단가(${Math.round(account.avgTicket / 10000)}만원)에 적합한 가격대(소매가 ${Math.round(wine.priceRetail / 1000)}천원)입니다.`
      );
    if (wine.tier === "Long Tail")
      reasons.push(
        `현재 재고 체류 ${wine.stockDays}일 초과 SKU이며, 이 업종에서 매칭 스코어가 높습니다.`
      );
    if (categoryScore >= 70)
      reasons.push(
        `${account.type}의 선호 카테고리(${account.preferredCategory[0]})에 부합합니다.`
      );
    if (industryScore >= 70)
      reasons.push(
        `${account.type}과 ${wine.country} 와인의 업종 궁합이 높습니다.`
      );
    if (reasons.length === 0)
      reasons.push(`매칭 알고리즘 기반으로 이 거래처에 적합한 와인입니다.`);

    return {
      wine,
      score: totalScore,
      reasons,
      priceScore: Math.round(priceScore),
      categoryScore: Math.round(categoryScore),
      industryScore: Math.round(industryScore),
      urgencyScore: Math.round(urgencyScore),
      marginScore: Math.round(marginScore),
    };
  });

  return results.sort((a, b) => b.score - a.score).slice(0, 8);
}
