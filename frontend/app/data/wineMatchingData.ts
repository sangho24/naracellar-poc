export interface Account {
  id: string;
  name: string;
  type: string;
  location: string;
  avgTicket: number; // 객단가 (원)
  monthlyVolume: number; // 월 판매 병수
  preferredCategory: string[];
  priceRange: { min: number; max: number }; // 소매가 기준 원
  currentTopSKUs: string[];
  channel: string;
}

export interface Wine {
  id: string;
  name: string;
  brand: string;
  country: string;
  region: string;
  grape: string;
  type: "레드" | "화이트" | "스파클링" | "로제" | "오렌지";
  priceRetail: number; // 소매가 원
  priceWholesale: number; // 도매가 원
  stockDays: number; // 현재 재고 체류일
  monthlyVelocity: number; // 월 판매량
  tier: "히트" | "중간" | "Long Tail";
  margin: number; // 마진율 0~1
}

export interface MatchResult {
  wine: Wine;
  score: number;
  reasons: string[];
  priceScore: number;
  categoryScore: number;
  industryScore: number;
  urgencyScore: number;
  marginScore: number;
}

